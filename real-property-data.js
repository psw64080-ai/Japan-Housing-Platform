function translateAmenities(jp) {
  if (!jp) return '';
  return jp.split(',').map(a => {
    const t = a.trim();
    for (const [jpKey, krVal] of Object.entries(AMENITY_JP_KR)) {
      if (t.includes(jpKey)) return t.replace(jpKey, krVal);
    }
    const base = t.replace(/\(.*?\)/, '').trim();
    const suffix = t.match(/\(.*?\)/)?.[0] || '';
    if (AMENITY_JP_KR[base]) return AMENITY_JP_KR[base] + suffix;
    return t;
  }).join(',');
}

